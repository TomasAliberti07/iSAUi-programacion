#Escribe un programa en Python que solicite al usuario que ingrese tres números 
#y luego calcule y muestre el promedio de esos números.
num1=float(input("Ingrese numero uno:"))
num2=float(input("Ingrese numero dos:"))
num3=float(input("Ingrese numero tres:"))
calculo=(num1+num2+num3)/3
print("El promedio total es de:", calculo)